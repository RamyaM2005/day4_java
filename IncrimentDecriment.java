import java.util.*;
class ncriment {
    public static void main(String args[])
    {
        Scanner sc=new Scanner(System.in);
        int a=sc.nextInt();
        int b=sc.nextInt();
        int c=sc.nextInt();
        int d=sc.nextInt();
        //Pre increment
        System.out.println(++a);
        //Post increment
        System.out.println(b++);
        //pre decrement
        System.out.println(--c);
        //post decrement
        System.out.println(d--);


    }        

}
